<CENTER><A NAME="L' africano"></A><h2>L'africano</h2></CENTER>

<DIV ALIGN=right><FONT SIZE=-1>(da Il Canzoniere di Riccardo Carlesso,
II)</FONT></DIV>
&nbsp;&nbsp;&nbsp; Certamente dedicata ad un amico del Carlesso, Francesco
Maria Cesari, &egrave; senz' altro composta in epoca pi&ugrave; tarda e
matura rispetto a "Un gatto". Risale certamente alla terza liceo del Carlesso,
poich&eacute; dice:"Due anni or son conobbi", e lo conobbe in prima liceo.
C' &egrave; qualche critico che nega la pelle nera del Cesari, considerandola
solo una caricatura, ma le fonti dell' epoca sembrano attestare il contrario.
Il significato dell' ultimo verso dovrebbe invece essere ironico, data
la profonda amicizia tra l' africano e il poeta. Da notare la differenza
tra l' articolo usato per identificare gatto e africano. "Un" potrebbe
essere stato usato per alludere a pi&ugrave; "gatti" (Buzzoni, Martini,
ecc.), mentre l' articolo determinativo davanti ad africano potrebbe alludere
all' originalit&agrave; e unicit&agrave; dell' elemento: forse il Cesari
viene dunque alluso come "fenomeno da baraccone".
<BR>Questo rimane uno dei pi&ugrave; importanti sonetti del Carlesso, ove
si nota una certa erudizione ed un sapere ancora troppo enciclopedico (ovvero
nozionistico), anche se &egrave; molto pi&ugrave; scorrevole del primo
sonetto.
<BR>&nbsp;
<BR>&nbsp;
<UL>&nbsp;&nbsp;&nbsp; Due anni or son conobbi
<BR>Un tale pelle-scura
<BR>Non lo facea per hobby
<BR>Eppur facea paura.
<P>&nbsp;&nbsp;&nbsp; Un fatto invero strano:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
5
<BR>I genitori infatti
<BR>Di quel bell'<SUP><FONT SIZE=-2>1</FONT></SUP> africano
<BR>Di latte paion fatti<SUP><FONT SIZE=-2>2</FONT></SUP>.
<P>&nbsp;&nbsp;&nbsp; Il Ku Klux Klan lo vuole
<BR>Eppur difficil pare -&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 10
<BR>Ch&eacute; tra le genti suole
<P>&nbsp;&nbsp;&nbsp; mimetizzarsi e stare -
<BR>Poi prender quella mole<SUP><FONT SIZE=-2>3</FONT></SUP>
<BR>Sar&agrave; gran brutto affare<SUP><FONT SIZE=-2>4</FONT></SUP>.
<BR>&nbsp;</UL>

<HR WIDTH="100%">
<BR>Metrica: Sonetto di settenari; rime: ABAB, CDCD, EFE, FEF.
<BR><FONT SIZE=-1><B>1 "bell'"</B>: Quel "bell'" &egrave; chiaramente ironico.</FONT>
<BR><FONT SIZE=-1><B>2 "di latte paion fatti"</B>: Diversamente dalla carnagione
del figlio, la loro &egrave; bianca: quindi, normale.</FONT>
<BR><FONT SIZE=-1><B>3 "mole"</B>: &egrave; controverso il significato
di questa parola. Potrebbe alludere ad un peso esagerato, ma, secondo le
fonti, l' obesit&agrave; &egrave; una dei pochi difetti che il Cesari non
aveva. Potrebbe dunque alludere alla somiglianza tra il Cesari e il Ghesini,
compagno di classe di entrambi (fino alla quarta liceo) che dimostrava
proprio questa malformazione. Altri critici pensano invece a "mole" come
una parola "rimediata" dal poeta tra le poche che facessero rima con "vuole"
e "suole". Dato il cos&igrave; alto livello artistico delle sue opere,
preferiamo comunque scartare codesta ipotesi.</FONT>
<BR><FONT SIZE=-1><B>4 "sar&agrave; gran brutto affare"</B>: "se anche
il Ku Klux Klan lo trovasse, ci rimetterebbe solamente". Forse il Carlesso
allude al fatto che il Cesari fosse un pessimo elemento.</FONT>
