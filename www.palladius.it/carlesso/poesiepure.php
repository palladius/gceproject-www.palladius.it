<CENTER><A NAME="Lo stagno"></A>

<h2>Lo stagno</h2></CENTER>

<DIV ALIGN=right><FONT SIZE=-1>(da Il Canzoniere di Riccardo Carlesso,
III)</FONT></DIV>
&nbsp;&nbsp;&nbsp; Mai lo sperimentalismo del Carlesso ha raggiunto un
culmine pi&ugrave; alto: la nuova forma metrica degli "esametri sfortunati"
(vedi <I>metrica</I>, sotto), il linguaggio pi&ugrave; basso, un forte
uso della ridondanza e di un ritmo frenetico, garantito dall' uso dell'
esametro al posto del pi&ugrave; elegante settenario, l' uso della poesia
nella poesia ("Puttana la rana", etc).
<BR>&nbsp;&nbsp;&nbsp; La trama &egrave; semplice: una rana, un ranocchio
e un girino stavano facendo il bagno in uno stagno. Improvvisamente arriva
una famiglia che rompe l' idillio: entra in acqua e disturba la famigliola.
La rana decide cos&igrave; di spaventarli e questi, impauriti, scappano

via.
<BR>&nbsp;&nbsp;&nbsp; E' un' opera altamente allegorica: pare infatti
che la rana, il ranocchio e il girino fossero il simbolo di tre compagni
di classe (due del liceo e uno delle medie) del Carlesso: la Chiara Bezzi
(cos&igrave; chiamata perch&eacute; di liberi costumi), Matteo Atti (cosiddetto
per le sue tendenze da "altra sponda"), e Marco Malagolini (per quest'
ultimo non &egrave; l' aggettivo a determinare la persona - nonostante
non fosse una cima - bens&igrave; l' animale stesso: veniva detto girino
perch&eacute; sembrava una larva, e il girino rende abbastanza l' idea).
<BR>&nbsp;&nbsp;&nbsp; Quest' opera &egrave;, inoltre, visibilmente didascalica:
il Carlesso tratta temi ormai dimenticati ma sempre attuali come il consumismo,
il poco rispetto dell' uomo verso la natura. Allegoricamente, d&agrave;
anche un segno "apocalittico": se l' uomo non star&agrave; attento la natura
gli si rivolter&agrave; contro (come la famigliola di rane ha fatto con
la famigliola di umani).
<BR>&nbsp;&nbsp;&nbsp; Duplice &egrave; dunque lo scopo del Carlesso: scherzoso
nei confronti degli amici, ma anche critico nelle sue invettive contro
l' uomo.
<BR>&nbsp;
<BR>&nbsp;
<BR>&nbsp;
<UL>&nbsp;&nbsp;&nbsp; Vidi in uno stagno
<BR>Una famigliola
<BR>- Di rane l&igrave; a bagno -
<BR>Beata e tutta sola<SUP><FONT SIZE=-2>1</FONT></SUP>.
<BR>&nbsp;
<P>&nbsp;&nbsp;&nbsp; Una famigliola&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
5
<BR>Di person (mi lagno)
<BR>Con la coca cola<SUP><FONT SIZE=-2>2</FONT></SUP>
<BR>Venne a far il bagno.
<P>&nbsp;&nbsp;&nbsp; Disturb&ograve; la rana
<BR>E cos&igrave; il ranocchio:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
10
<BR>Persino il girino.
<P>&nbsp;&nbsp;&nbsp; "Puttana la rana!"
<BR>"Finocchio il ranocchio!"
<BR>"Cretino il girino!"
<P>&nbsp;&nbsp;&nbsp; Urlaron in coro.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
15
<BR>Usciron poi loro
<BR>Con poco decoro<SUP><FONT SIZE=-2>3</FONT></SUP>.
<BR>&nbsp;</UL>

<HR WIDTH="100%">
<BR>Metrica: esametri sfortunati (cos&igrave; chiamati perch&eacute; in
17 versi; secondo le fonti dell' epoca il 17 portava sfortuna); rime: ABAB,
ABAB, CDE, CDE, FFF.
<BR><FONT SIZE=-1><B>1 "Vidi... sola"</B>: Questi primi quattro versi vogliono
costruire una situazione idilliaca e sottolineare la presa di posizione
del poeta a favore degli animali contro l' uomo (vedi introduzione).</FONT>
<BR><FONT SIZE=-1><B>2 "con la coca cola"</B>: Non vuol tanto dire che
andarono a fare il bagno con la lattina. Qui l' autore vuole rappresentare
con la coca cola il consumismo dell' uomo (vedi introduzione).</FONT>
<BR><FONT SIZE=-1><B>3 "con poco decoro"</B>: La famiglia usc&igrave; tanto
spaventata che si sarebbe fatta compatire se ci fosse stato qualcun altro
in giro.</FONT>
<BR>
