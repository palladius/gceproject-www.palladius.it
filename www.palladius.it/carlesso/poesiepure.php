<CENTER><A NAME="Un gatto"></A><h2>Un gatto</h2></CENTER>

<DIV ALIGN=right><FONT SIZE=-1>(da Il Canzoniere di Riccardo Carlesso,
I)</FONT></DIV>
&nbsp;&nbsp;&nbsp; Una delle prime poesie (e prima del canzoniere) di Riccardo
Carlesso, uno dei poeti che pi&ugrave; ha caratterizzato la letteratura
del fine Novecento, &egrave; senza dubbio molto spensierata e gioiosa.
Grazie ai settenari, forme metriche tra le pi&ugrave; veloci, &egrave;
riuscito a dare un ritmo particolare a questa poesia, che diviene quasi
musicale. Il primo posto dato a questo sonetto nel Canzoniere proverebbe
un profondo legame affettivo tra i due. E' un sonetto sperimentale gi&agrave;
a partire dalla metrica: le rime sono invertite e stravolte. Protagonista
del sonetto &egrave; un certo gatto, probabile metafora, secondo le fonti,
con cui soleva chiamare un suo amico, Alessandro Buzzoni. Altri critici
attesterebbero il nome, invece, a Jacopo Martini, altro amico del Carlesso.
Il <I>cursus</I> inizia lentamente, e diviene via via pi&ugrave; veloce.
Le rime lo assecondano: da un iniziale ABAB, si passa ad un sempre pi&ugrave;
frenetico e ridondante GGG. Importante &egrave; poi il termine "gattata":
l' autore ne parla come di una cosa fatta con furbizia ma che si rivela
un fallimento: cosa che quindi si dimostra non cos&igrave; furba. Secondo
le fonti dell' epoca, un fatto simile era stato fatto da Martini (cosa
che sposterebbe le ipotesi sulla metafora del gatto da Buzzoni a lui).
Sembra che fosse arrivato a scuola in ritardo per evitare un' interrogazione
e che si fosse messo ad origliare per ascoltare quando il professore finiva
d' interrogare. Udito che si sarebbe andati avanti a spiegare, sarebbe
entrato per poi essere prontamente interrogato dal professore (o <I>prof</I>,
come nello slang degli anni 80-90). Altri esempi (meno celeberrimi) di
gattata sarebbero un<I> fuoco</I> (vedi note per il significato) di un
certo Jonata Costa, visto dal suo professore di matematica e dalla sua
preside, mentre aspettava un suo amico dopo l' entrata da scuola.
<BR>&nbsp;&nbsp;&nbsp; Senz' altro composto in et&agrave; giovanile, rimane
uno dei pi&ugrave; importanti sonetti del Carlesso, ove si nota una certa
erudizione ed un sapere ancora troppo enciclopedico (ovvero nozionistico).
<BR>&nbsp;
<UL>&nbsp;&nbsp;&nbsp; Ier vidi un certo gatto,
<BR>Iocava co' compagni<SUP><FONT SIZE=-2>1</FONT></SUP>
<BR>Felice e tutto matto,
<BR>Non fe' di che mi lagni<SUP><FONT SIZE=-2>2</FONT></SUP>.
<P>&nbsp;&nbsp;&nbsp; Quo modo<SUP><FONT SIZE=-2>3</FONT></SUP> l' appellammo&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
5
<BR>Ch&egrave;<SUP><FONT SIZE=-2>4</FONT></SUP> fu felino e scaltro,
<BR>Ma pi&ugrave; per qualcos' altro:
<BR>Gattata la chiamammo.
<P>&nbsp;&nbsp;&nbsp; E' azion di gran furbizia
<BR>Che infin invece sfuma:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 10
<BR>Si ve' ch' era fittizia<SUP><FONT SIZE=-2>5</FONT></SUP>.
<P>&nbsp;&nbsp;&nbsp; In fuoco fu beccato<SUP><FONT SIZE=-2>6</FONT></SUP>,
<BR>Non pronto interrogato<SUP><FONT SIZE=-2>7</FONT></SUP>,
<BR>Infine rimandato<SUP><FONT SIZE=-2>8</FONT></SUP>.
<BR>&nbsp;</UL>

<HR WIDTH="100%"><FONT SIZE=-1><B>1 "Iocava co' compagni"</B>: Da notare
l' assonanza: ca, co, co.</FONT>
<BR><FONT SIZE=-1><B>2 "Non fe' di che mi lagni"</B>: Controversa l' interpretazione
di questo verso; probabilmente: "Non aveva mai fatto qualcosa di cui mi
dovessi dispiacere". Questo verso &egrave; l' unico indizio della grande
amicizia che doveva esserci tra l' autore e "il gatto".</FONT>
<BR><FONT SIZE=-1><B>3 "Quo modo"</B>: "In questo modo"; da notare la reminiscenza
latineggiante.</FONT>
<BR><FONT SIZE=-1><B>4 "Ch&egrave;"</B>: "poich&eacute;".</FONT>
<BR><FONT SIZE=-1><B>5 "Si ve' ch' era fittizia"</B>: "Da ci&ograve; si
deduce che doveva non essere vera". "Si ve'" &egrave; la contrazione di
"si vede".</FONT>
<BR><FONT SIZE=-1><B>6 "In fuoco fu beccato"</B>: "fu colto mentre faceva
fuoco". "Fuoco" fa parte dello slang di fine Novecento molto usato dagli
studenti. Vuol dire marinare la scuola.</FONT>
<BR><FONT SIZE=-1><B>7 "Non pronto interrogato"</B>: "interrogato quando
non era pronto". Controversa anche l' interpretazione di questo verso.
Non si sa se il Carlesso alludesse al fatto che Alessandro Buzzoni non
studiasse mai o al fatto che fosse interrogato sempre quando, di rado,
non si era preparato. Per la profonda vicinanza tra il Carlesso e l' amico,
si &egrave; pi&ugrave; propensi ad accogliere la seconda ipotesi.</FONT>
<BR><FONT SIZE=-1><B>8 "infine rimandato"</B>: "alla fine dell' anno fu
rimandato a settembre".</FONT>
<BR>
