<h2>Il lupo</h2></CENTER>

<DIV ALIGN=right><FONT SIZE=-1>(da Il Canzoniere di Riccardo Carlesso,
V)</FONT></DIV>
&nbsp;&nbsp;&nbsp; Altro sonetto ermetico del Carlesso con una nuova sfumatura
metrica che denota la sinossi formale nelle poesie che gli permette di
adattare rime diverse a diverse esigenze. Il lupo in questione &egrave;
chiaramente l'amico Costa Jonata del quale l'Autore vuole darci una migliore
caratterizzazione. Nella prima quartina comincia col caratterizzare il
lupo con melanconia e quasi compassione: &egrave; diverso dai colleghi,
perch&eacute; timido: &egrave; perci&ograve; un emarginato dalla societ&agrave;;
si passa da una piccola situazione ad una forte invettiva contro la societ&agrave;
dell'epoca. Nella seconda quartina si passa ad una durezza spietata che
si ricollega al tema dell'amarezza. Il tono torna spensierato nelle ultime
due terzine.
<BR>&nbsp;
<BR>&nbsp;
<UL>&nbsp;&nbsp;&nbsp; Conobbi un lupacchiotto
<BR>Diverso da colleghi:
<BR>Ei era un timidotto
<BR>Difficil che vi spieghi<SUP><FONT SIZE=-2>1</FONT></SUP>.
<P>&nbsp;&nbsp;&nbsp; I' penso sotto sotto&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
5
<BR>(Ma non che me ne freghi<SUP><FONT SIZE=-2>2</FONT></SUP>)
<BR>Che qui si fosse rotto<SUP><FONT SIZE=-2>3</FONT></SUP>,
<BR>Ma non che non lo neghi<SUP><FONT SIZE=-2>4</FONT></SUP>.
<P>&nbsp;&nbsp;&nbsp; E' invero s&igrave; peloso:
<BR>Un amuleto<SUP><FONT SIZE=-2>5</FONT></SUP> appare&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
10
<BR>Nel petto s&igrave; villoso.
<P>&nbsp;&nbsp;&nbsp; Non pu&ograve; andare al mare
<BR>Poich&eacute; il pi&ugrave; coraggioso
<BR>Non pu&ograve; far che scappare.
<BR>&nbsp;</UL>

<HR WIDTH="100%">
<BR>Metrica: Sonetto di settenari; rime: ABAB, CDCD, EFE, FEF.
<BR><FONT SIZE=-1><B>1 "Difficil che vi spieghi"</B>: E' tanto timido che
&egrave; difficile darne un'idea. Questa &egrave; una <I>chiarissima</I>
invettiva contro lo Stilnovo, che predicava l' ineffabilit&agrave; della
donna (cio&egrave; l'incapacit&agrave; di descrivere le sue qualit&agrave;).</FONT>
<BR><FONT SIZE=-1><B>2 "(Ma non che me ne freghi)"</B>: ma ci&ograve; non
m'interessa. Qui l'Autore con abile sapienza narrativa cerca di distaccarsi
e di prendere le distanze dall'oggetto del discorso, in modo da dirlo in
maniera pi&ugrave; oggettiva.</FONT>
<BR><FONT SIZE=-1><B>3 "Che qui si fosse rotto":</B> Che si fosse stancato
di stare a scuola.</FONT>
<BR><FONT SIZE=-1><B>4 "Ma non che non lo neghi:</B> Ma, nonostante sia
un pensiero, lo nego. Difficilissimo verso di cui solo il Carlesso poteva
avere un'idea, date le sue eccezionali doti matematiche, umanistiche, musicali,
intellettive, sinottiche, misogine, introspettive, ineffabili, quantistiche,
annichilistiche e dialettiche (come ci perviene da un autocommento dell'Autore
contenuto in uno dei suoi tanti frammenti).</FONT>
<BR><FONT SIZE=-1><B>5 "amuleto"</B>: nel petto il Costa pare avere un
insieme di peli della forma simile ad un amuleto.</FONT>
<BR>
