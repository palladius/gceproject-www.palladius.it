<CENTER><A NAME="Il buffone"></A><h2>Il buffone</h2></CENTER>

<DIV ALIGN=right><FONT SIZE=-1>(da Il Canzoniere di Riccardo Carlesso,
IV)</FONT></DIV>
&nbsp;&nbsp;&nbsp; Vengono presi nuovi temi in questo sonetto eccezionale
per la sua dinamicit&agrave; e per i suoi concetti fusi con una tale sapienza
narrativa da rasentare la cultura Palumbea (come ci dice l'Autore in frammenti
andati perduti). Innanzitutto il tema dell' <I>amarezza</I>, come definita
dall'Autore stesso: la descrizione della classe e del suo amico sono tutt'
altro che positive. E' facile capire a chi si riferisse l'Autore nel sonetto
in chiave ermetica, che segue quel tipo di canto carnascialesco - come
precisa l'autore - che non c'entra niente ma fa capire il suo alto livello
d'erudizione. E' l'amico Matteo Atti. Molti indizi ci portano a lui: buffone,
"uom", l'aria da baro, le due "pale" che fa venir su, la parvenza di coglione,
contrapposta ai modi ben educati.
<BR>&nbsp;&nbsp;&nbsp; Importante &egrave; l' accenno a "togate" e "palliate",
che l'autore sfrutta sapientemente per due motivi: per fare dell'erudizione
e per collegare il tema della commedia (ci&ograve; che, teoricamente, deve
fare ridere) all' Atti.
<BR>&nbsp;
<BR>&nbsp;
<UL>&nbsp;&nbsp;&nbsp; Frequento classe tale
<BR>Che circo l' appellaro<SUP><FONT SIZE=-2>1</FONT></SUP>
<BR>Amico a me s&igrave; caro<SUP><FONT SIZE=-2>2</FONT></SUP>
<BR>A simil rango sale<SUP><FONT SIZE=-2>3</FONT></SUP>.
<P>&nbsp;&nbsp;&nbsp; Non che ce l' abbia a male&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
5
<BR>Con tale "uom"<SUP><FONT SIZE=-2>4</FONT></SUP> ignaro
<BR>Ch'ha l'aria d'un ver baro:
<BR>Venir fa su due pale<SUP><FONT SIZE=-2>5</FONT></SUP>.
<P>&nbsp;&nbsp;&nbsp; A buon ragion buffone
<BR>Codesto fu appellato:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
10
<BR>Sa dir solo togate<SUP><FONT SIZE=-2>6</FONT></SUP>.
<P>&nbsp;&nbsp;&nbsp; Costui pare un coglione
<BR>Sebben ben educato.
<BR>S&igrave;: come ne'e palliate<SUP><FONT SIZE=-2>7</FONT></SUP>.
<BR>&nbsp;</UL>

<HR WIDTH="100%">
<BR>Metrica: Sonetto di settenari; rime: ABBA, CDDC, EFG, EFG.
<BR><FONT SIZE=-1><B>1 "Che circo l' appellaro"</B>: Con <I>amarezza</I>,
il Carlesso parla delle condizioni non proprio di normalit&agrave; della
propria classe.</FONT>
<BR><FONT SIZE=-1><B>2 "Amico a me s&igrave; caro"</B>: Indica grande vicinanza
affettiva tra il Carlesso e l' Atti, probabilmente legata alla speranza
di scroccare (cio&egrave; chiedere qualcosa ad un altro come per carit&agrave;)
parte delle sue merende (consistenti in pinzoni e pizze). Il Carlesso &egrave;
infatti tra i pochi Umanisti non disposti a prendere i voti minori per
trarre una migliore sussistenza.</FONT>
<BR><FONT SIZE=-1><B>3 "A simil rango sale"</B>: Assurge a rango di buffone,
appunto.</FONT>
<BR><FONT SIZE=-1><B>4 "uom"</B>: Il Carlesso, con molta amarezza, sottolinea
la difficolt&agrave; nel distinguere il sesso dell'Atti, per i suoi modi
fuorvianti.</FONT>
<BR><FONT SIZE=-1><B>5 "Venir fa su due pale"</B>: Contestatissima la traduzione
di questo verso: secondo filologi e puristi, allora era in uso la frase
poco fine: "Far venir su due palle", ove le palle alludono alle gonadi
maschili. La frase vuol dire: arrecare fastidio. Con grande sapienza retorica
e narrativa l'Autore avrebbe contratto "palle" in "pale" per fare rima
con "tale","sale" e "male".</FONT>
<BR><FONT SIZE=-1><B>6 "Togate"</B>: Commedie di stampo romanizzante.</FONT>
<BR><FONT SIZE=-1><B>7 "Palliate"</B>: Commedie di stampo grecizzante.</FONT>
<BR>
