<CENTER><A NAME="La mongolfiera"></A>
<h2>La mongolfiera</h2></CENTER>

<DIV ALIGN=right><FONT SIZE=-1>(da Il Canzoniere di Riccardo Carlesso,
VI)</FONT></DIV>
&nbsp;&nbsp;&nbsp; E' con questo capolavoro che l'Autore comincia a fare
suoi gl'insegnamenti dei grandi maestri del passato e a tentare un eccezionale
e ben riuscito sperimentalismo: l'indovinello carlessiano. Si tratta di
