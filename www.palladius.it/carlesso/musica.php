sperando che sta volta ci prenda), il bassista? Giovanni Myiung &egrave;
inutile come ogni bassista: basta fare t&ugrave;-t&ugrave; su una corda
a caso (donde la famosa definizione di Random Bass Player). Quanto all'ultimo
album, <i>Falling into infinity</i>,&nbsp; non &egrave; affatto male, anche
se si sono un po' commercializzati (troppi 4/4!), ma le ballate non mancano
neanche qui con le splendide <i>Anna Lee</i>, <i>Hollow Years</i>... A
